package org.example.Calculation;

public class calc {

    public int addition(int n , int m){
        return n+m;
    }
    public int subtraction(int n , int m){
        return n-m;
    }
    public int multiply(int n , int m){
        return n*m;
    }
    public int division(int n , int m){
        return  n/m;
    }

}
